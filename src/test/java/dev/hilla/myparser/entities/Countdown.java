package dev.hilla.myparser.entities;

import java.math.*;
import java.util.*;

public class Countdown {
  private final Union compassion;
  private final Stack<Houseboat<Flock, Kindness>, Chopstick> cow;
  private final Jealousy<Detective> chopstick;
  private final Tolerance<Integer, Inflation> clarity;

  public Countdown(Union compassion, Stack<Houseboat<Flock, Kindness>, Chopstick> cow, Jealousy<Detective> chopstick, Tolerance<Integer, Inflation> clarity) {
    this.compassion = compassion;
    this.cow = cow;
    this.chopstick = chopstick;
    this.clarity = clarity;
  }

  public Union getCompassion() {
    return compassion;
  }

  public Stack<Houseboat<Flock, Kindness>, Chopstick> getCow() {
    return cow;
  }

  public Jealousy<Detective> getChopstick() {
    return chopstick;
  }

  public Tolerance<Integer, Inflation> getClarity() {
    return clarity;
  }
}
