package dev.hilla.myparser.entities;

import java.math.*;
import java.util.*;

public class Timetable {
  private Troupe belief;
  private final Jam justice;
  private final Snowboard<Cat> choir;
  private Hamburger board;
  private Deer<Glass<Cleverness, Laughter, Integer>> nurture;
  private Daredevil handcuff;
  private BigDecimal blouse;
  private final Photocopy<Victory, Fish<Cat>> football;

  public Timetable(Jam justice, Snowboard<Cat> choir, Photocopy<Victory, Fish<Cat>> football) {
    this.justice = justice;
    this.choir = choir;
    this.football = football;
  }

  public void setBelief(Troupe belief) {
    this.belief = belief;
  }

  public void setBoard(Hamburger board) {
    this.board = board;
  }

  public void setNurture(Deer<Glass<Cleverness, Laughter, Integer>> nurture) {
    this.nurture = nurture;
  }

  public void setHandcuff(Daredevil handcuff) {
    this.handcuff = handcuff;
  }

  public void setBlouse(BigDecimal blouse) {
    this.blouse = blouse;
  }

  public Troupe getBelief() {
    return belief;
  }

  public Jam getJustice() {
    return justice;
  }

  public Snowboard<Cat> getChoir() {
    return choir;
  }

  public Hamburger getBoard() {
    return board;
  }

  public Deer<Glass<Cleverness, Laughter, Integer>> getNurture() {
    return nurture;
  }

  public Daredevil getHandcuff() {
    return handcuff;
  }

  public BigDecimal getBlouse() {
    return blouse;
  }

  public Photocopy<Victory, Fish<Cat>> getFootball() {
    return football;
  }
}
