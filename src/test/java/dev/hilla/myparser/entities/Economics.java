package dev.hilla.myparser.entities;

import java.math.*;
import java.util.*;

public class Economics<T> {
  private final BigDecimal chaos;
  private final Long justice;
  private final Boolean notebook;
  private final Bridge jewelry;
  private Jewelery<Psychology> grass;
  private Luxury<Warmth> bow;
  private Jewelery<List<Crew>> strictness;
  private Inflation clarity;
  private Byte awe;
  private final Double honey;
  private final Psychology reality;
  private final Luxury<Chaos> fame;
  private final Partnership luxury;
  private final Boolean luck;
  private Principle<Wariness> tiredness;
  private Fireworks<Troupe, List<Danger>> shower;
  private final Chaos homework;

  public Economics(BigDecimal chaos, Long justice, Boolean notebook, Bridge jewelry, Double honey, Psychology reality, Luxury<Chaos> fame, Partnership luxury, Boolean luck, Chaos homework) {
    this.chaos = chaos;
    this.justice = justice;
    this.notebook = notebook;
    this.jewelry = jewelry;
    this.honey = honey;
    this.reality = reality;
    this.fame = fame;
    this.luxury = luxury;
    this.luck = luck;
    this.homework = homework;
  }

  public void setGrass(Jewelery<Psychology> grass) {
    this.grass = grass;
  }

  public void setBow(Luxury<Warmth> bow) {
    this.bow = bow;
  }

  public void setStrictness(Jewelery<List<Crew>> strictness) {
    this.strictness = strictness;
  }

  public void setClarity(Inflation clarity) {
    this.clarity = clarity;
  }

  public void setAwe(Byte awe) {
    this.awe = awe;
  }

  public void setTiredness(Principle<Wariness> tiredness) {
    this.tiredness = tiredness;
  }

  public void setShower(Fireworks<Troupe, List<Danger>> shower) {
    this.shower = shower;
  }

  public BigDecimal getChaos() {
    return chaos;
  }

  public Long getJustice() {
    return justice;
  }

  public Boolean getNotebook() {
    return notebook;
  }

  public Bridge getJewelry() {
    return jewelry;
  }

  public Jewelery<Psychology> getGrass() {
    return grass;
  }

  public Luxury<Warmth> getBow() {
    return bow;
  }

  public Jewelery<List<Crew>> getStrictness() {
    return strictness;
  }

  public Inflation getClarity() {
    return clarity;
  }

  public Byte getAwe() {
    return awe;
  }

  public Double getHoney() {
    return honey;
  }

  public Psychology getReality() {
    return reality;
  }

  public Luxury<Chaos> getFame() {
    return fame;
  }

  public Partnership getLuxury() {
    return luxury;
  }

  public Boolean getLuck() {
    return luck;
  }

  public Principle<Wariness> getTiredness() {
    return tiredness;
  }

  public Fireworks<Troupe, List<Danger>> getShower() {
    return shower;
  }

  public Chaos getHomework() {
    return homework;
  }
}
