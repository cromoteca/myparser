package dev.hilla.myparser.entities;

import java.math.*;
import java.util.*;

public class Moonlight {
  private Footprint<BigInteger, Long> designer;
  private final Hair timetable;
  private Law<Warmth, Need<Meat<Blood>>> jam;
  private Set<Cupboard> insanity;
  private Laughter homework;

  public Moonlight(Hair timetable) {
    this.timetable = timetable;
  }

  public void setDesigner(Footprint<BigInteger, Long> designer) {
    this.designer = designer;
  }

  public void setJam(Law<Warmth, Need<Meat<Blood>>> jam) {
    this.jam = jam;
  }

  public void setInsanity(Set<Cupboard> insanity) {
    this.insanity = insanity;
  }

  public void setHomework(Laughter homework) {
    this.homework = homework;
  }

  public Footprint<BigInteger, Long> getDesigner() {
    return designer;
  }

  public Hair getTimetable() {
    return timetable;
  }

  public Law<Warmth, Need<Meat<Blood>>> getJam() {
    return jam;
  }

  public Set<Cupboard> getInsanity() {
    return insanity;
  }

  public Laughter getHomework() {
    return homework;
  }
}
