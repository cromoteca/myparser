package dev.hilla.myparser.entities;

import java.math.*;
import java.util.*;

public class Fan {
  private Homework freezer;
  private final Stack<Horsefly, Satisfaction> horsefly;
  private final Wariness duty;
  private Film guest;
  private final Childhood cupboard;
  private Inflation kindness;
  private Bed<Daredevil> bermudas;
  private final Troupe union;

  public Fan(Stack<Horsefly, Satisfaction> horsefly, Wariness duty, Childhood cupboard, Troupe union) {
    this.horsefly = horsefly;
    this.duty = duty;
    this.cupboard = cupboard;
    this.union = union;
  }

  public void setFreezer(Homework freezer) {
    this.freezer = freezer;
  }

  public void setGuest(Film guest) {
    this.guest = guest;
  }

  public void setKindness(Inflation kindness) {
    this.kindness = kindness;
  }

  public void setBermudas(Bed<Daredevil> bermudas) {
    this.bermudas = bermudas;
  }

  public Homework getFreezer() {
    return freezer;
  }

  public Stack<Horsefly, Satisfaction> getHorsefly() {
    return horsefly;
  }

  public Wariness getDuty() {
    return duty;
  }

  public Film getGuest() {
    return guest;
  }

  public Childhood getCupboard() {
    return cupboard;
  }

  public Inflation getKindness() {
    return kindness;
  }

  public Bed<Daredevil> getBermudas() {
    return bermudas;
  }

  public Troupe getUnion() {
    return union;
  }
}
