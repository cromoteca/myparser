package dev.hilla.myparser.entities;

import java.math.*;
import java.util.*;

public class Opportunity {
  private Industry<Chaos> relaxation;
  private final Adulthood dresser;
  private Desk<Dancing<Industry<Crime>>> beans;
  private final Horsefly classmate;
  private final Fame love;
  private Gang<Houseboat<Chaos, Cabinet>> blouse;
  private Law<Rattlesnake, List<Growth>> troupe;

  public Opportunity(Adulthood dresser, Horsefly classmate, Fame love) {
    this.dresser = dresser;
    this.classmate = classmate;
    this.love = love;
  }

  public void setRelaxation(Industry<Chaos> relaxation) {
    this.relaxation = relaxation;
  }

  public void setBeans(Desk<Dancing<Industry<Crime>>> beans) {
    this.beans = beans;
  }

  public void setBlouse(Gang<Houseboat<Chaos, Cabinet>> blouse) {
    this.blouse = blouse;
  }

  public void setTroupe(Law<Rattlesnake, List<Growth>> troupe) {
    this.troupe = troupe;
  }

  public Industry<Chaos> getRelaxation() {
    return relaxation;
  }

  public Adulthood getDresser() {
    return dresser;
  }

  public Desk<Dancing<Industry<Crime>>> getBeans() {
    return beans;
  }

  public Horsefly getClassmate() {
    return classmate;
  }

  public Fame getLove() {
    return love;
  }

  public Gang<Houseboat<Chaos, Cabinet>> getBlouse() {
    return blouse;
  }

  public Law<Rattlesnake, List<Growth>> getTroupe() {
    return troupe;
  }
}
