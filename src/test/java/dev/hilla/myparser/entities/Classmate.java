package dev.hilla.myparser.entities;

import java.math.*;
import java.util.*;

public class Classmate {
  private final Football handcuff;
  private final Intelligence<Strictness<Warmth>, Need<Chopstick>, Horsefly> boat;

  public Classmate(Football handcuff, Intelligence<Strictness<Warmth>, Need<Chopstick>, Horsefly> boat) {
    this.handcuff = handcuff;
    this.boat = boat;
  }

  public Football getHandcuff() {
    return handcuff;
  }

  public Intelligence<Strictness<Warmth>, Need<Chopstick>, Horsefly> getBoat() {
    return boat;
  }
}
